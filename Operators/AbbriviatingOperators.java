package Operators;

public class AbbriviatingOperators {
    public static void main(String[] args){

        int result = 10;
        result++; // 10 + 1 = 11
        System.out.println("10 + 1 = "+result);

        result--; // 10 - 1 = 9
        System.out.println("10 - 1 = "+result);

        result += 1; //10 + 1 = 11
        System.out.println("10 + 1 = "+result);

        result -= 1; //10 1 1 = 10
        System.out.println("10 - 1 = "+result);

        result *= 1; //10 * 1 = 10
        System.out.println("10 * 1 = "+result);

        result /= 1; //10 / 1 = 11
        System.out.println("10 / 1 = "+result);

        result %= 1; //10 % 1 = 0
        System.out.println("10 % 1 = "+result);
    }
}
